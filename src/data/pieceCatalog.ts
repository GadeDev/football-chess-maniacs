export const PIECE_CATALOG_LIST = Object.values(PIECE_CATALOG).sort((a, b) => a.id - b.id);

export function getPieceCatalogEntry(pieceId: number): PieceCatalogEntry | undefined {
  return PIECE_CATALOG[pieceId];
}
